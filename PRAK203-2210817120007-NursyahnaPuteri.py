
a = float (input ("masukan nilai pertama : "))
b = float (input ("masukan nilai kedua : "))
i = float (input ("masukan nilai ketiga : "))
j = float (input ("masukan nilai keempat : "))
x = float (input ("masukan nilai kelima : "))
y = float (input ("masukan nilai keenam : "))

hasil = (a - b) * (i / j) - (x + y )
print ("Hasil dari perhitungan tersebut adalah {:.3f}".format (hasil))